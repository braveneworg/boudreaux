/**
 * Mint a FRESH throwaway better-auth session for the dedicated sign-out user and
 * return a Playwright storageState carrying its signed session cookie. Each call
 * uses a unique token, so the destructive sign-out specs never share a session —
 * with each other (parallel CI workers) or across a retry — and signing one out
 * can never revoke a session another spec relies on.
 *
 * Uses the same isolated E2E Prisma client as the seed (datasourceUrl pinned to
 * the local Docker Mongo) — never the ambient DATABASE_URL — and the shared
 * {@link E2E_AUTH_SECRET} so the cookie verifies against the web server.
 */
const createDisposableSignoutState = async (): Promise<
  Awaited<ReturnType<typeof createStorageState>>
> => {
  const token = `e2e-signout-disposable-${randomUUID()}`;
  const prisma = new PrismaClient({ datasourceUrl: E2E_DATABASE_URL });
  try {
    await prisma.session.create({
      data: { token, userId: SIGNOUT_USER.id, expiresAt: sessionExpiresAt() },
    });
  } finally {
    await prisma.$disconnect();
  }
  return createStorageState(token, E2E_AUTH_SECRET);
};

/**
 * Wait for the MongoDB replica set to be ready. The Docker healthcheck
 * initiates `rs.initiate()` but there is a race — Prisma can connect
 * before the replica set election completes. This helper retries a
 * lightweight write until it succeeds or the timeout is exceeded.
 */
const waitForReplicaSet = async (prisma: PrismaClient, maxWaitMs = 30_000): Promise<void> => {
  const start = Date.now();
  const retryIntervalMs = 1_000;

  while (Date.now() - start < maxWaitMs) {
    try {
      // A simple findMany doesn't require transactions; use deleteMany on a
      // likely-empty collection to exercise the write path that needs the RS.
      await prisma.verification.deleteMany({ where: { identifier: '__rs_probe__' } });
      return;
    } catch (error: unknown) {
      const message = error instanceof Error ? error.message : String(error);
      const isReplicaSetNotReady =
        message.includes('Transactions are not supported') ||
        message.includes('not primary') ||
        message.includes('node is recovering');

      if (!isReplicaSetNotReady) {
        throw error; // unexpected error — don't swallow it
      }

      console.info(
        `Waiting for MongoDB replica set to be ready... (${Math.round((Date.now() - start) / 1000)}s)`
      );
      await new Promise((resolve) => setTimeout(resolve, retryIntervalMs));
    }
  }

  throw new Error(
    `MongoDB replica set was not ready after ${maxWaitMs / 1000}s. ` +
      'Ensure the Docker container is running: pnpm run e2e:docker:up'
  );
};

