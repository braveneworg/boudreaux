import { Dawning_of_a_New_Day } from 'next/font/google';
import { headers } from 'next/headers';
import { userAgentFromString } from 'next/server';

import { Separator } from '@radix-ui/react-separator';

import { Toaster } from '@/components/ui/sonner';

import AuthToolbar from './components/auth/auth-toolbar';
import Footer from './components/footer/footer';
import Header from './components/header/header';
import { Providers } from './components/providers';

import type { Metadata } from 'next';

import './globals.css';

// Server-side environment validation on startup
if (typeof window === 'undefined') {
  // Dynamic import to avoid bundling in client
  import('@/app/lib/config/env-validation').then(({ validateEnvironment }) => {
    validateEnvironment();
  });
}

// Client-side: Detect and warn about HTTPS/HTTP mismatch in development
if (typeof window !== 'undefined' && process.env.NODE_ENV === 'development') {
  if (window.location.protocol === 'https:' && window.location.hostname === 'localhost') {
    console.error(
      'ðŸš¨ HTTPS DETECTED: You are accessing localhost via HTTPS. ' +
        'This will cause authentication and API issues. ' +
        'Please use http://localhost:3000 instead.'
    );
  }
}

